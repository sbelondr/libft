#include "../headers/libft.h"

void	ft_pow_array_fix(int i, char (*nb)[BUFF_S])
{
	int	cnt;

	cnt = 0;
	ft_bzero(&(*nb), BUFF_S);
	(*nb)[cnt] = '5';
	while (i-- >= 0)
		(*nb)[++cnt] = '0';
}
